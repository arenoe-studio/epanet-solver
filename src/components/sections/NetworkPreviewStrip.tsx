function PrvSymbol({ x, y }: { x: number; y: number }) {
  const s = 5;
  return (
    <g transform={`translate(${x},${y})`}>
      <polygon points={`${-s},${-s} 0,0 ${-s},${s}`} fill="#1a1a1a" stroke="none" />
      <polygon points={`${s},${-s} 0,0 ${s},${s}`}  fill="#1a1a1a" stroke="none" />
    </g>
  );
}

function ReservoirSymbol({ x, y }: { x: number; y: number }) {
  return (
    <g>
      <rect x={x - 6} y={y - 5} width={12} height={10} fill="#f3f4f6" stroke="#1a1a1a" strokeWidth={1.2} />
      <path d={`M${x - 4},${y + 1} Q${x},${y - 2} ${x + 4},${y + 1}`} stroke="#1a1a1a" strokeWidth={1} fill="none" />
    </g>
  );
}

