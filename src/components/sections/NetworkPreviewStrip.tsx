type Coord = [number, number];

const X_MIN = 10500, X_RANGE = 7900;
const Y_MAX_C = 28700, Y_RANGE = 9200;
const SVG_W = 620, SVG_H = 720, PAD = 20;
const UW = SVG_W - 2 * PAD, UH = SVG_H - 2 * PAD;

const tx = (x: number) => PAD + ((x - X_MIN) / X_RANGE) * UW;
const ty = (y: number) => PAD + ((Y_MAX_C - y) / Y_RANGE) * UH;

function polyPts(from: Coord, verts: Coord[], to: Coord): string {
  return [from, ...verts, to]
    .map(([x, y]) => `${tx(x).toFixed(1)},${ty(y).toFixed(1)}`)
    .join(" ");
}

